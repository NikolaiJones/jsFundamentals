// ASSIGNMENT OPERATOR
let r = 5;

//ADDITION ASSIGNMENT
r += 1; // r = r_i +1 -> r = 5 + 1

// SUBTRACTION OPERATOR
r -= 1; // r = r_i - 1 -> r = 5 - 1

//MULTIPLICATION OPERATOR
r *= 1; // r = r_i * 1

//DIVISION OPERATOR
r /= 1; // r = r_i /1

// REMAINDER (or modulus) ASSIGNMENT
r %= 1; // r = r_i % 1

// EXPONENTIAL ASSIGNMENT
r **= 1; // r = r_i ** 1
